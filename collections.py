# Collections examples

# List
my_colors = ["red", "blue", "orange", "yellow"]

print("\n", my_colors)

# add vr insert
my_colors.append("purple")
my_colors.insert(3, "brown")

print("\n", my_colors)

# remove
my_colors.remove("brown")
my_colors.pop()
del my_colors[1]

print("\n", my_colors)


